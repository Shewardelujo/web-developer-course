//.includes()
//used on strings and arrays
"Helloooooo".includes("o"); //returns true, because the string includes 'o'

const pets = ["cat", "dog", "bat"];
pets.includes("dogs"); //true
pets.includes("bird"); //false

//**, meaning raise to the power of
const square = (x) => x ** 2;
square(2); //returns 4
square(3); //returns 9

const cube = (y) => y ** 3;
cube(3); //returns 27
cube(4); //returns 64
